import time
import os

def animate_ascii(art, mode='grow', delay=0.5, clear=False):
    """
    Animate ASCII art.
    
    Parameters:
        art (str or list): The ASCII art to animate.
        mode (str): 'line' for line-by-line reveal, 'grow' for grow effect.
        delay (float): Time in seconds between lines/steps.
        clear (bool): Whether to clear the screen between steps.
    """
    if isinstance(art, str):
        lines = art.splitlines()
    else:
        lines = art  # Assume it's already a list of lines

    if mode == 'line':
        for line in lines:
            print(line)
            time.sleep(delay)
    elif mode == 'grow':
        for i in range(1, len(lines) + 1):
            if clear:
                os.system('cls' if os.name == 'nt' else 'clear')
            print("\n".join(lines[:i]))
            time.sleep(delay)
    else:
        raise ValueError("Invalid mode. Use 'line' or 'grow'.")
    

